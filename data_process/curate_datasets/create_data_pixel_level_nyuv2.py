#!/usr/bin/env python
"""
Create pixel-level depth dataset metadata for NYU Depth V2 (extracted from SUN RGB-D).

Directory structure (generated by extract_nyuv2_from_sunrgbd.py):
    nyuv2_extracted/
    ├── train/
    │   ├── rgb/NYU{xxxx}.jpg
    │   ├── depth/NYU{xxxx}.png          (uint16, /1000 = meters)
    │   └── intrinsics/NYU{xxxx}.json    → [fx, fy, cx, cy, W, H]
    └── test/
        └── (same as above)

Usage:
    python create_data_pixel_level_nyuv2.py \
        --data_root /path/to/nyuv2_extracted \
        --output_dir ./annotations/nyuv2
"""
import argparse
import json
import os

from tqdm import tqdm

DEPTH_SCALE = 8000.0  # SUN RGB-D NYU depth_bfx: uint16 / 10000 = meters
CANONICAL_FX = 1000.0


def process_frame(data_root, split, name):
    """Process a single frame and return a record or None."""
    rgb_path = os.path.join(data_root, split, "rgb", f"{name}.jpg")
    depth_path = os.path.join(data_root, split, "depth", f"{name}.png")
    intr_path = os.path.join(data_root, split, "intrinsics", f"{name}.json")

    if not os.path.exists(rgb_path) or not os.path.exists(depth_path):
        return None

    # Read intrinsics: [fx, fy, cx, cy, W, H]
    fx = None
    rgb_w, rgb_h = None, None
    if os.path.exists(intr_path):
        with open(intr_path, "r") as f:
            intr = json.loads(f.read())
        fx = intr[0]
        rgb_w, rgb_h = int(intr[4]), int(intr[5])

    if rgb_w is None:
        return None

    # Compute canonical size
    if fx and fx > 0:
        scale = CANONICAL_FX / fx
        canonical_w = round(rgb_w * scale)
        canonical_h = round(rgb_h * scale)
    else:
        canonical_w = canonical_h = None

    return {
        "image": f"nyuv2_extracted/{split}/rgb/{name}.jpg",
        "depth_path": f"nyuv2_extracted/{split}/depth/{name}.png",
        "depth_scale": DEPTH_SCALE,
        "original_rgb_size": [rgb_w, rgb_h],
        "original_depth_size": [rgb_w, rgb_h],
        "original_fx": round(fx, 2) if fx else None,
        "canonical_fx": CANONICAL_FX,
        "canonical_size": [canonical_w, canonical_h] if canonical_w else None,
        "prompt": "Given this image, estimate the metric depth (in meters, rounded to two decimal places) for every pixel of the image.",
        "solution": "OK, I will estimate the depth map of this image.",
    }


def main():
    parser = argparse.ArgumentParser(description="Create pixel-level depth metadata for NYU v2.")
    parser.add_argument("--data_root", type=str, required=True,
                        help="Root dir of nyuv2_extracted/ (contains train/, test/)")
    parser.add_argument("--output_dir", type=str, required=True)
    args = parser.parse_args()

    os.makedirs(args.output_dir, exist_ok=True)

    for split in ["train", "test"]:
        rgb_dir = os.path.join(args.data_root, split, "rgb")
        if not os.path.isdir(rgb_dir):
            print(f"WARNING: {rgb_dir} not found, skipping {split}")
            continue

        names = sorted([
            os.path.splitext(f)[0] for f in os.listdir(rgb_dir)
            if f.endswith((".jpg", ".png"))
        ])
        print(f"\n[{split}] Found {len(names)} images")

        records = []
        for name in tqdm(names, desc=split):
            record = process_frame(args.data_root, split, name)
            if record is not None:
                records.append(record)

        output_path = os.path.join(args.output_dir, f"nyuv2_pixel_depth_{split}.jsonl")
        with open(output_path, "w") as f:
            for r in records:
                f.write(json.dumps(r, ensure_ascii=False) + "\n")

        print(f"{split}: {len(records)} frames → {output_path}")
        if records:
            fxs = [r["original_fx"] for r in records if r.get("original_fx")]
            if fxs:
                print(f"  fx range: [{min(fxs):.1f}, {max(fxs):.1f}]")
            sizes = set(tuple(r["original_rgb_size"]) for r in records)
            print(f"  RGB resolutions: {sizes}")
            canonical_sizes = set(tuple(r["canonical_size"]) for r in records if r.get("canonical_size"))
            if canonical_sizes:
                print(f"  Canonical sizes: {sorted(canonical_sizes)}")


if __name__ == "__main__":
    main()
